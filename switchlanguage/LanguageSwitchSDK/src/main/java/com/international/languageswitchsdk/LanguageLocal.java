package com.international.languageswitchsdk;

import java.util.Locale;

/**
 * 对于Locale类中没有定义的语言常量 我们在这里手动定义
 */
public class LanguageLocal {
    //西班牙语
    public static Locale Spanish = new Locale("es", "", "");
    // 印地语，印度官方语言之一
    public static Locale Hindi = new Locale("hi", "", "");
    // 印尼语
    public static Locale Indonestian = new Locale("in", "", "");
    // 葡萄牙语
    public static Locale Portuguess = new Locale("pt", "", "");
    // 俄罗斯语
    public static Locale Russian = new Locale("ru", "", "");

}